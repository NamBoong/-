package codeup;

import java.util.Scanner;

public class Main1081 {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		
		int n=scan.nextInt();
		int m=scan.nextInt();
		
		for(int a=1; a<=n; a++) {
		for(int b=1; b<=m; b++) {

			System.out.println(a + " " + b);

			}
			}
		scan.close();
	}
}

